console.log(Math.max(1,2,3,4,5,-10,-50,1500,9,8,7,6));
//menor numero da sequencia
console.log(Math.min(1,2,3,4,5,-10,-50,1500,9,8,7,6));
//gerar numero aleatorio
console.log(Math.random()*(10 - 5)+5);
//Valor de pi
console.log(Math.PI);
